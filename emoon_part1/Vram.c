#include "Vram.h"

static u32 curr_p = 0;

///////////////////////////////////////////////////////////////////////////////
// Vram_Reset()
///////////////////////////////////////////////////////////////////////////////

void Vram_Reset()
{
  curr_p = 0;
}

///////////////////////////////////////////////////////////////////////////////
// Vram_Alloc()
///////////////////////////////////////////////////////////////////////////////

u32 Vram_Alloc(u32 size)
{
  u32 new_p;

  new_p = curr_p;
  curr_p += (size);

  if( curr_p & 0x1FFF )
	{
		curr_p = (curr_p & 0xFFFFE000) + 0x2000;
	}

  return new_p;
}

